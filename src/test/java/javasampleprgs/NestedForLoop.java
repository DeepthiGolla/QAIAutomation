package javasampleprgs;

public class NestedForLoop 
{
	
	//for each i value, J will repeat until condition fails at J 

	public static void main(String[] args) 
	{
		
		for(int i=1;i<=3;i++)
		{  
			
		
			//loop of j  
			for(int j=1;j<=3;j++)
			{  
			        System.out.println(i+" "+j);  

			} // J loop 
			        
			        
		} //i loop 
} //Main method 
	
	
} // class